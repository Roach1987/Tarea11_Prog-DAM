    /**
     * Método auxiliar que limpia la tabla.
     */
    private void borrarTabla(){
        int longitud = tablaListaAvion.getRowCount()-1;  
        for(int i = longitud; i >= 0; i--){
            modelo.removeRow(i);
        }
    }
    
    /**
     * Método que carga los articulos que se encuentran en la lista temporal,
     * y recoge los datos mas importantes para cargarlos en la tabla.
     */
    private void cargarListaAviones(){
        // Liampiamos la tabla al cargar nuevos aviones, para evitar duplicidades.
        if(existenDatos){
            borrarTabla();
        }
        
        List<Avion> listaAviones = AvionDAO.recuperarAviones();
        
        for(Avion avion : listaAviones){
            // Creamos un array para recoger los datos necesarios de cada avion.
            String numeroPlazas = String.valueOf(avion.getNumeroPlazas());
            String [] fila = {avion.getCodigo(), avion.getModelo(), numeroPlazas};
    
            // Añadimos a la tabla los datos de la cuenta actual.
            modelo.addRow(fila);
        }
    }

